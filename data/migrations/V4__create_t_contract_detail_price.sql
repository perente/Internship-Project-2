CREATE TABLE T_CONTRACT_DETAIL_PRICE (
    ID                  VARCHAR2(255) NOT NULL,
    CREATE_TIME         TIMESTAMP,
    CREATE_USER         VARCHAR2(255),
    CREATE_USERIP       VARCHAR2(255),
    DELETED             VARCHAR2(255),
    UPDATE_TIME         TIMESTAMP,
    UPDATE_USER         VARCHAR2(255),
    UPDATE_USERIP       VARCHAR2(255),
    VERSION             NUMBER(10,0),
    BILLING_SERVICE_CODE VARCHAR2(255),
    BILLING_TARIFF_CODE VARCHAR2(255),
    CAMPAIGN_START_DATE DATE,
    PROMO_CODE_REF_ID   VARCHAR2(255),
    VARIABLE_PRICE      VARCHAR2(255),
    CURRENCY            VARCHAR2(255),
    PAYMENT_PLAN_TEMPL_ID VARCHAR2(255),
    PRICE_LIST_ID       VARCHAR2(255),
    MIG_STATUS          VARCHAR2(2),
    MIG_DATE            DATE,
    PRICE_AMOUNT        NUMBER(19,6),

    CONSTRAINT PK_T_CONTRACT_DETAIL_PRICE PRIMARY KEY (ID),

    -- CHECK constraints
    CONSTRAINT CHK_VAR_PRICE CHECK (VARIABLE_PRICE IN ('YES','NO','UNDEFINED'))
);



