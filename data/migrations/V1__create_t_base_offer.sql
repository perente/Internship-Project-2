-- V1: create T_BASE_OFFER 

CREATE TABLE T_BASE_OFFER (
  ID                          VARCHAR2(255) NOT NULL,
  CREATE_TIME                 TIMESTAMP,
  CREATE_USER                 VARCHAR2(255),
  CREATE_USERIP               VARCHAR2(255),
  DELETED                     VARCHAR2(255),
  UPDATE_TIME                 TIMESTAMP,
  UPDATE_USER                 VARCHAR2(255),
  UPDATE_USERIP               VARCHAR2(255),
  VERSION                     NUMBER(10,0),
  ACTIVE                      VARCHAR2(255),
  CODE                        VARCHAR2(255),
  DESCRIPTION_EN              VARCHAR2(255),
  DESCRIPTION_TR              VARCHAR2(255),
  NAME                        VARCHAR2(255),
  SORT_ORDER                  NUMBER(10,0),
  AUTO_ID                     NUMBER(19,0),
  SHOW_ON_PROPOSAL            VARCHAR2(255),
  STATUS                      VARCHAR2(255),
  MIG_STATUS                  VARCHAR2(2),
  MIG_DATE                    DATE,
  DOCUMENT_SET_ID             VARCHAR2(255),
  CONTRACT_PERIOD_ID          VARCHAR2(255),
  COMMITMENT_CAN_BE_MODIFY    VARCHAR2(10),
  ACTUAL_STRT_DATE_CAN_BE_MODIFY VARCHAR2(10),
  FULFILLMENT_ITEM_CODE       VARCHAR2(255),
  SHOW_END_DATE               VARCHAR2(10),
  BILLING_END_OF_MONTH        VARCHAR2(10),
  CHECK_ACTIVE_PEND_NUM_TRANSFER VARCHAR2(10),
  SET_TYPE_ID                 VARCHAR2(255),
  NCST_ID                     VARCHAR2(255),
  CAN_DOWNGRADE               VARCHAR2(10),
  BILL_SHIPPING_METHOD_ID     VARCHAR2(255),
  CLOSE_TO_SALE_IN_BUNDLE     VARCHAR2(255),
  TFS_LIMIT                   NUMBER(19,2) DEFAULT 0,

  /* primary key */
  CONSTRAINT PK_T_BASE_OFFER PRIMARY KEY (ID),

  /* NOT NULL’lar (Oracle bunları CHECK ile gösterir; biz isim veriyoruz) */
  CONSTRAINT NN_T_BASE_OFFER_ID            CHECK (ID IS NOT NULL),
  CONSTRAINT NN_T_BASE_OFFER_CODE          CHECK (CODE IS NOT NULL),
  CONSTRAINT NN_T_BASE_OFFER_DESC_TR       CHECK (DESCRIPTION_TR IS NOT NULL),
  CONSTRAINT NN_T_BASE_OFFER_NAME          CHECK (NAME IS NOT NULL),
  CONSTRAINT NN_T_BASE_OFFER_SORT_ORDER    CHECK (SORT_ORDER IS NOT NULL),

  /* enum tarzı alanlar */
  CONSTRAINT CHK_T_BASE_OFFER_STATUS
    CHECK (STATUS IN ('ACTIVE','INACTIVE','RETIRED','CLOSED_TO_SELL')),

  CONSTRAINT CHK_T_BASE_OFFER_ACTIVE
    CHECK (ACTIVE IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_SHOW_ON_PROPOSAL
    CHECK (SHOW_ON_PROPOSAL IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_CLOSE_TO_SALE
    CHECK (CLOSE_TO_SALE_IN_BUNDLE IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_ACTUAL_STRT_DATE_CAN_BE_MODIFY
    CHECK (ACTUAL_STRT_DATE_CAN_BE_MODIFY IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_COMMITMENT_CAN_BE_MODIFY
    CHECK (COMMITMENT_CAN_BE_MODIFY IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_BILLING_END_OF_MONTH
    CHECK (BILLING_END_OF_MONTH IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_CHECK_ACTIVE_PEND_NUM_TRANSFER
    CHECK (CHECK_ACTIVE_PEND_NUM_TRANSFER IN ('YES','NO','UNDEFINED')),

  CONSTRAINT CHK_T_BASE_OFFER_CAN_DOWNGRADE
    CHECK (CAN_DOWNGRADE IN ('YES','NO','UNDEFINED'))
);


