-- V4: create T_CONTRACT_DETAIL_DISCOUNT

CREATE TABLE T_CONTRACT_DETAIL_DISCOUNT (
    ID                        VARCHAR2(255) NOT NULL,
    CREATE_TIME               TIMESTAMP,
    CREATE_USER               VARCHAR2(255),
    CREATE_USERIP             VARCHAR2(255),
    DELETED                   VARCHAR2(255),
    UPDATE_TIME               TIMESTAMP,
    UPDATE_USER               VARCHAR2(255),
    UPDATE_USERIP             VARCHAR2(255),
    VERSION                   NUMBER(10,0),
    AUTO_ID                   NUMBER(19,0),
    COMMITMENT_DISCOUNT       VARCHAR2(255),
    DISCOUNT_BILLING_CODE     VARCHAR2(255),
    DISCOUNT_STATUS           VARCHAR2(255),
    PERIOD_END                NUMBER(10,0),
    PERIOD_START              NUMBER(10,0),
    CONTRACT_DETAIL_PRICE_ID  VARCHAR2(255),
    MIG_STATUS                VARCHAR2(2),
    MIG_DATE                  DATE,
    AMOUNT                    NUMBER(19,6),
    CALCULATION_TYPE          VARCHAR2(255),
    BULK_BENEFIT_ID           VARCHAR2(255),
    NONCOMMITMENT_DISCOUNT_ID VARCHAR2(255),
    DISCOUNT_START_DATE       DATE,
    DISCOUNT_END_DATE         DATE,

    CONSTRAINT PK_T_CONTRACT_DETAIL_DISCOUNT PRIMARY KEY (ID),

    -- CHECK constraints
    CONSTRAINT CHK_COMMITMENT_DISCOUNT CHECK (COMMITMENT_DISCOUNT IN ('YES','NO','UNDEFINED')),
    CONSTRAINT CHK_DISCOUNT_STATUS CHECK (DISCOUNT_STATUS IN ('ACTIVE','DEACTIVE'))
);





