-- V6: create T_BUNDLE_OFFER 

CREATE TABLE T_BUNDLE_OFFER (
  BILLING_CODE                       VARCHAR2(255)   NOT NULL,
  BILLING_DESCRIPTION                VARCHAR2(255),
  END_DATE                           TIMESTAMP       NOT NULL,
  PLSLN_SUIT                         VARCHAR2(255),
  START_DATE                         TIMESTAMP       NOT NULL,
  ID                                 VARCHAR2(255)   NOT NULL, 
  BUNDLE_OFFER_GROUP_ID              VARCHAR2(255),
  BUNDLE_PRODUCT_ID                  VARCHAR2(255)   NOT NULL,
  MIG_STATUS                         VARCHAR2(2),
  MIG_DATE                           DATE,
  DOWNGRADE_START_DATE               TIMESTAMP,
  BUNDLE_OFFER_CAMPAIGN_ID           VARCHAR2(255),
  CAMPAIGN_CATEGORY_ID               VARCHAR2(255),
  DISABLE_UNDO_CAMPAIGN_CHANGE       VARCHAR2(10),
  IS_BRAND_MODEL_BASED_BUNDLE        VARCHAR2(10),
  IS_MIN_USAGE_EXEMPTED              VARCHAR2(10),
  COMMIT_TRANS_ON_PROD_CHANGE        VARCHAR2(10),
  IS_INFRASTRUCTURE_BASED            VARCHAR2(10),
  CMPGN_CHANGE_END_OF_COMMITMENT     VARCHAR2(255),
  PRIORITY                           NUMBER,
  PROMOCODE_OFFER_CODE               VARCHAR2(255),
  EXTRAJET_URL                       VARCHAR2(1024),
  UNDO_AVAILABILITY                  VARCHAR2(255),
  COMMITMENT_CHECK_DAY               NUMBER DEFAULT NULL,
  BTK_REP_NEEDED                     VARCHAR2(10),
  SOLNET_URL                         VARCHAR2(400),
  CAMPAIGN_CRITERIA_TEXT             VARCHAR2(4000),
  IS_REFURBISHED_MODEM_CAMPAIGN      VARCHAR2(10),

  CONSTRAINT PK_T_BUNDLE_OFFER PRIMARY KEY (ID),

  /* NOT NULL kontrolleri */
  CONSTRAINT NN_TBO_ID                    CHECK (ID IS NOT NULL),
  CONSTRAINT NN_TBO_BILLING_CODE          CHECK (BILLING_CODE IS NOT NULL),
  CONSTRAINT NN_TBO_END_DATE              CHECK (END_DATE IS NOT NULL),
  CONSTRAINT NN_TBO_START_DATE            CHECK (START_DATE IS NOT NULL),
  CONSTRAINT NN_TBO_BUNDLE_PRODUCT_ID     CHECK (BUNDLE_PRODUCT_ID IS NOT NULL),

  /* enum benzeri alanlar */
  CONSTRAINT CHK_TBO_BTK_REP_NEEDED           CHECK (BTK_REP_NEEDED IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_CMPGN_END_OF_COMMITMENT  CHECK (CMPGN_CHANGE_END_OF_COMMITMENT IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_COMMIT_TRANS_ON_PROD     CHECK (COMMIT_TRANS_ON_PROD_CHANGE IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_DISABLE_UNDO_CAMP        CHECK (DISABLE_UNDO_CAMPAIGN_CHANGE IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_BRAND_BASED_BUNDLE       CHECK (IS_BRAND_MODEL_BASED_BUNDLE IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_INFRASTRUCTURE_BASED     CHECK (IS_INFRASTRUCTURE_BASED IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_MIN_USAGE_EXEMPTED       CHECK (IS_MIN_USAGE_EXEMPTED IN ('YES','NO','UNDEFINED')),
  CONSTRAINT CHK_TBO_PLSLN_SUIT               CHECK (PLSLN_SUIT IN ('YES','NO','UNDEFINED'))
);




