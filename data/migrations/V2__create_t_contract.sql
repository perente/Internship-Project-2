-- V2: create T_CONTRACT

CREATE TABLE T_CONTRACT (
  ID                      VARCHAR2(255) NOT NULL,
  CREATE_TIME             TIMESTAMP,
  CREATE_USER             VARCHAR2(255),
  CREATE_USERIP           VARCHAR2(255),
  DELETED                 VARCHAR2(255),
  UPDATE_TIME             TIMESTAMP,
  UPDATE_USER             VARCHAR2(255),
  UPDATE_USERIP           VARCHAR2(255),
  VERSION                 NUMBER(10,0),
  AUTO_ID                 NUMBER(19,0),
  BILLING_CONTRACT_ID     VARCHAR2(255),
  CONTRACT_STATUS         VARCHAR2(255),
  END_DATE                DATE,
  START_DATE              DATE,
  SUIT_PACKAGE_DEFINITION VARCHAR2(255),
  CUSTOMER_ID             VARCHAR2(255),
  MIG_STATUS              VARCHAR2(2),
  MIG_DATE                DATE,

  -- constraints
  CONSTRAINT PK_T_CONTRACT PRIMARY KEY (ID),
  CONSTRAINT CHK_T_CONTRACT_STATUS CHECK (CONTRACT_STATUS IN ('ACTIVE','PASSIVE'))
);

-- opsiyonel indexler
CREATE INDEX IX_T_CONTRACT_CUSTOMER_ID         ON T_CONTRACT (CUSTOMER_ID);
CREATE INDEX IX_T_CONTRACT_BILLING_CONTRACT_ID ON T_CONTRACT (BILLING_CONTRACT_ID);
CREATE INDEX IX_T_CONTRACT_STATUS              ON T_CONTRACT (CONTRACT_STATUS);
