CREATE TABLE T_DISCOUNT (
    ID                  VARCHAR2(255) PRIMARY KEY,
    CREATE_TIME         TIMESTAMP,
    CREATE_USER         VARCHAR2(255),
    CREATE_USERIP       VARCHAR2(255),
    DELETED             VARCHAR2(255),
    UPDATE_TIME         TIMESTAMP,
    UPDATE_USER         VARCHAR2(255),
    UPDATE_USERIP       VARCHAR2(255),
    VERSION             NUMBER(10,0),
    ACTIVE              VARCHAR2(255) CHECK (ACTIVE IN ('YES','NO','UNDEFINED')),
    CODE                VARCHAR2(255) NOT NULL,
    DESCRIPTION_EN      VARCHAR2(255),
    DESCRIPTION_TR      VARCHAR2(255) NOT NULL,
    NAME                VARCHAR2(80) NOT NULL,
    SORT_ORDER          NUMBER(10,0) NOT NULL,
    AUTO_ID             NUMBER(19,0),
    CALCULATION_TYPE    VARCHAR2(255),
    COMMITMENT_DISCOUNT VARCHAR2(255) CHECK (COMMITMENT_DISCOUNT IN ('YES','NO','UNDEFINED')),
    PERIOD_END          NUMBER(10,0),
    PERIOD_START        NUMBER(10,0),
    PERIOD_TYPE         NUMBER(10,0),
    PRICE_LIST_ID       VARCHAR2(255),
    MIG_STATUS          VARCHAR2(2),
    MIG_DATE            DATE,
    AMOUNT              NUMBER(19,6),
    MIN_QUANTITY        NUMBER(38,0),
    MAX_QUANTITY        NUMBER(38,0),
    IS_GRADUATED        VARCHAR2(255) CHECK (IS_GRADUATED IN ('YES','NO','UNDEFINED'))
);


